package com.dam.theorigin.model;

public class FileContentDetails {
	
	private String replicaServerPath;
	private String originFileName;
	
	public String getReplicaServerPath() {
		return replicaServerPath;
	}
	public void setReplicaServerPath(String replicaServerPath) {
		this.replicaServerPath = replicaServerPath;
	}
	public String getOriginFileName() {
		return originFileName;
	}
	public void setOriginFileName(String originFileName) {
		this.originFileName = originFileName;
	}
	
	
}
